    private void listPersonals() {
        List<Personals> personals = daoPersonals.getListPersonals();

        DefaultTableModel modelTable = new DefaultTableModel(
                null,
                new Object[]{"ID", "CI", "FIRSTNAME", "LASTNAME", "PHONE", "ADDRESS", "BIRTHDAY"}
        );

        personals.forEach((value) -> {
            modelTable.addRow(new Object[]{value.getId(), value.getCi(), value.getFirstName(), value.getLastName(), value.getPhone(), value.getAddress(), value.getBirthday()});
        });

        jTable1.setModel(modelTable);
    }

    private void savePersonal() {
        personal = new Personals(txtPersonalCI.getText(), txtPersonalFirsName.getText(), txtPersonalLastName.getText(), txtPersonalPhone.getText(), txtPersonalAddress.getText(), txtPersonalBirthday.getDate());
        daoPersonals.insert(personal);
        bodyContent.setSelectedComponent(personalsIndex);
        this.listPersonals();
    }

    private void selectPersonal() {
        int row = jTable1.getSelectedRow();
        if (row > -1) {
            try {
                txtPersonalID.setText(jTable1.getValueAt(row, 0).toString());
                txtPersonalCI.setText(jTable1.getValueAt(row, 1).toString());
                txtPersonalFirsName.setText(jTable1.getValueAt(row, 2).toString());
                txtPersonalLastName.setText(jTable1.getValueAt(row, 3).toString());
                txtPersonalPhone.setText(jTable1.getValueAt(row, 4).toString());
                txtPersonalAddress.setText(jTable1.getValueAt(row, 5).toString());

                Date date = new SimpleDateFormat("yyyy-MM-dd").parse(jTable1.getValueAt(row, 6).toString());
                txtPersonalBirthday.setDate(date);
            } catch (ParseException ex) {
                System.out.println(ex.getMessage());
            }
        }

    }

    private void updatePersonal() {
        personal = new Personals(txtPersonalCI.getText(), txtPersonalFirsName.getText(), txtPersonalLastName.getText(), txtPersonalPhone.getText(), txtPersonalAddress.getText(), txtPersonalBirthday.getDate());
        personal.setId(Integer.parseInt(txtPersonalID.getText()));
        
        daoPersonals.update(personal);
        
        bodyContent.setSelectedComponent(personalsIndex);
        this.listPersonals();
    }

